void main() {
  // print('Working');
  // MAP
  // Dart map is an object store for data in key-value form/pair.
  // Similar to objects in JavaScript
  //
  Map student = {'name': 'Mohsin', 'age': 20};

  // To print whole Map
  print(student);

  // To print all keys
  print(student.keys);

  // To print all keys values
  print(student.values);

  // To print any specific key by name
  print(student['name']);
  print(student['age']);
}
